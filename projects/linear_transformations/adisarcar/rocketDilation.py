import numpy as np
import matplotlib.pyplot as plt

rocket = np.array([
    [0, 2, 2,  1, 0, 0],  # X coordinates
    [0, 0, 4,  6, 4, 0]   # Y coordinates (taken from AI google gemini)
])

dilMatrix = np.array([[1/2,0],[0,2]]) # dilation matrix
newRocket = dilMatrix @ rocket # transform the rocket

# Plot original rocket (red to represent initial state)
plt.plot(rocket[0], rocket[1], color='red')

# Plot transformed rocket (blue to represent transformed)
plt.plot(newRocket[0], newRocket[1], color='blue')
plt.gca().set_aspect('equal')
plt.show()

