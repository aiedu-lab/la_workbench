import numpy as np
import matplotlib.pyplot as plt
a1 = np.array([3,0])
a2 = np.array([0,4])
a3 = np.array([2,-2])
a4 = np.array([5,-1])
a5 = a1 + a2 + a3 + a4
print(a5)
print(np.linalg.norm(a5)>=(np.linalg.norm(a4)+np.linalg.norm(a3)+np.linalg.norm(a2)+np.linalg.norm(a1)))
plt.quiver(0, 0, a1[0], a1[1], angles='xy', scale_units='xy', scale=1, color='blue')
plt.quiver(a1[0], a1[1], a2[0], a2[1], angles='xy', scale_units='xy', scale=1, color='red')
plt.quiver((a1+a2)[0], (a1+a2)[1], a3[0], a3[1], angles='xy', scale_units='xy', scale=1, color='green')
plt.quiver((a1+a2+a3)[0], (a1+a2+a3)[1], a4[0], a4[1], angles='xy', scale_units='xy', scale=1, color='purple')
plt.xlim(-1, 10)
plt.ylim(-3, 10)
plt.gca().set_aspect('equal')
plt.grid(True)
plt.show()

