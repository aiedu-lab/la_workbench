def elimLast(arr1,arr2,var): # eliminates last variable, returns an array2
  return arr1 - arr2 * ((float)(arr1[var])/arr2[var])

# reduce 6-var system of equations to a 5-var
refList = []

def findRef(refList,var):
  for i in range (0,len(c)):
    if c[i][var] != 0 and i not in refList:
      refList.append(i)
      break

def elimVar(refList,cRef,var):
  for i in range (0,len(c)):
    if(i not in refList and c[i][var] != 0):
      c[i] = elimLast(c[cRef],c[i],var)


for var in range (5,-1,-1):
  findRef(refList,var)
  cRef = refList[len(refList)-1]
  elimVar(refList,cRef,var)
